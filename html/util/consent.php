    <h5>5. ระยะเวลาในการเก็บรักษาข้อมูลส่วนบุคคล</h5>
    <label>
    ร้านค้าจะเก็บรักษาข้อมูลส่วนบุคคลตราบเท่าที่จำเป็นต่อการประมวลผล และเมื่อพ้นระยะเวลาดังกล่าวแล้ว ร้านค้าจะดำเนินการทำลายข้อมูลส่วนบุคคล
    </label>
</div>
<div class="mb-2" name="consent_6">
    <h5>6. สิทธิของเจ้าของข้อมูลส่วนบุคคล</h5>
    <label>
    ความยินยอมที่เจ้าของข้อมูลส่วนบุคคลให้ไว้กับร้านค้าในการเก็บรวบรวม ใช้ และเปิดเผยข้อมูลส่วนบุคคลยังคงใช้ได้จนกว่าเจ้าของข้อมูลส่วนบุคคลจะเพิกถอนความยินยอมเป็นลายลักษณ์อักษร โดยเจ้าของข้อมูลส่วนบุคคลสามารถเพิกถอนความยินยอมหรือระงับการใช้หรือเปิดเผยข้อมูลส่วนบุคคล เพื่อวัตถุประสงค์ในการดำเนินกิจกรรมใด ๆ หรือทุกกิจกรรมของร้านค้า โดยส่งคำขอของเจ้าของข้อมูลส่วนบุคคลแจ้งให้ร้านค้าทราบเป็นลายลักษณ์อักษรหรือผ่านทางจดหมายอิเล็กทรอนิกส์ pumin.s@ku.th
    นอกจากสิทธิดังกล่าวข้างต้น เจ้าของข้อมูลส่วนบุคคลยังมีสิทธิในการดำเนินการ ดังต่อไปนี้
    </label>
    <label>
    (1) สิทธิในการเพิกถอนความยินยอม (right to withdraw consent)
    </label>
    <label>
    เจ้าของข้อมูลส่วนบุคคลมีสิทธิเพิกถอนความยินยอมในการประมวลผลข้อมูลส่วนบุคคลที่เจ้าของข้อมูลส่วนบุคคลได้ให้ความยินยอมไว้แก่ร้านค้าได้ตลอดระยะเวลาที่ข้อมูลส่วนบุคคลของตนอยู่กับร้านค้า
    </label>
    <label>
    (2) สิทธิในการเข้าถึงข้อมูลส่วนบุคคล (right of access)
    </label>
    <label>
    เจ้าของข้อมูลส่วนบุคคลมีสิทธิเข้าถึงข้อมูลส่วนบุคคลของตนและขอให้ร้านค้าทำสำเนาข้อมูลส่วนบุคคลดังกล่าวให้แก่เจ้าของข้อมูลส่วนบุคคล รวมถึงขอให้ร้านค้าเปิดเผยการได้มาซึ่งข้อมูลส่วนบุคคลที่เจ้าของข้อมูลส่วนบุคคลไม่ได้ให้ความยินยอมต่อร้านค้าได้
    </label>
    <label>
    (3) สิทธิในการแก้ไขข้อมูลส่วนบุคคลให้ถูกต้อง (right to rectification)
    </label>
    <label>
    เจ้าของข้อมูลส่วนบุคคลมีสิทธิขอให้ร้านค้าแก้ไขข้อมูลส่วนบุคคลที่ไม่ถูกต้อง หรือเพิ่มเติมข้อมูลส่วนบุคคลที่ไม่สมบูรณ์
    </label>
    <label>
    (4) สิทธิในการลบข้อมูลส่วนบุคคล (right to erasure)
    </label>
    <label>
    เจ้าของข้อมูลส่วนบุคคลมีสิทธิในการขอให้ร้านค้าทำการลบข้อมูลส่วนบุคคลของตนด้วยเหตุบางประการได้
    </label>
    <label>
    (5) สิทธิในการระงับการใช้ข้อมูลส่วนบุคคล (right to restriction of processing)
    </label>
    <label>
    เจ้าของข้อมูลส่วนบุคคลมีสิทธิระงับการใช้ข้อมูลส่วนบุคคลของตนด้วยเหตุบางประการได้
    </label>
    <label>
    (6) สิทธิในการให้โอนย้ายข้อมูลส่วนบุคคล (right to data portability)
    </label>
    <label>
    เจ้าของข้อมูลส่วนบุคคลมีสิทธิให้ร้านค้าโอนย้ายข้อมูลส่วนบุคคลซึ่งเจ้าของข้อมูลส่วนบุคคลได้ให้ไว้กับร้านค้าไปยังผู้ควบคุมข้อมูลส่วนบุคคลรายอื่น หรือตัวเจ้าของข้อมูลส่วนบุคคลเองด้วยเหตุบางประการได้
    </label>
    <label>
    (7) สิทธิในการคัดค้านการประมวลผลข้อมูลส่วนบุคคล (right to object)
    </label>
    <label>
    เจ้าของข้อมูลส่วนบุคคลมีสิทธิคัดค้านการประมวลผลข้อมูลส่วนบุคคลของตนด้วยเหตุบางประการได้
    </label>
    <label>
    ร้านค้าเคารพการตัดสินใจการเพิกถอนความยินยอมของเจ้าของข้อมูลส่วนบุคคล แต่อย่างไรก็ตาม ร้านค้าขอแจ้งให้เจ้าของข้อมูลส่วนบุคคลทราบว่าอาจมีข้อจำกัดสิทธิในการเพิกถอนความยินยอมโดยกฎหมายหรือสัญญาที่ให้ประโยชน์แก่เจ้าของข้อมูลส่วนบุคคล ทั้งนี้ การเพิกถอนความยินยอมย่อมไม่ส่งผลกระทบต่อการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลที่เจ้าของข้อมูลส่วนบุคคลได้ให้ความยินยอมไว้แล้ว
    </label>
</div>
<div class="mb-2" name="consent_7">
    <h5>7. การรักษาความมั่นคงปลอดภัยสำหรับข้อมูลส่วนบุคคล</h5>
    <label>
    ร้านค้าจัดให้มีมาตรการการรักษาความมั่นคงปลอดภัยที่เหมาะสม เพื่อป้องกัน การเข้าถึง การใช้ การเปลี่ยนแปลง การแก้ไข หรือการเปิดเผยข้อมูลส่วนบุคคลโดยปราศจากอำนาจหรือโดยมิชอบ นอกจากนี้ ร้านค้าได้กำหนดแนวปฏิบัติภายในร้านค้าเพื่อกำหนดสิทธิในการเข้าถึงหรือการใช้ข้อมูลส่วนบุคคลของเจ้าของข้อมูลส่วนบุคคล เพื่อรักษาความลับและความปลอดภัยของข้อมูล และร้านค้าจะจัดให้มีการทบทวนมาตรการดังกล่าวเป็นระยะเพื่อความเหมาะสม
    </label>
</div>
<div class="mb-2" name="consent_8">
    <h5>8. การใช้คุกกี้ (Cookies)</h5>
    <label>
    คุกกี้ (Cookies) หมายถึง ข้อมูลขนาดเล็กที่เว็บไซต์ส่งไปเก็บไว้กับเจ้าของข้อมูลส่วนบุคคลที่เข้าชมเว็บไซต์ เพื่อช่วยให้เว็บไซต์จดจำข้อมูลเข้าชมของเจ้าของข้อมูลส่วนบุคคล หรือการตั้งค่าอื่น ๆ เมื่อเจ้าของข้อมูลส่วนบุคคลเข้าชมเว็บไซต์ในครั้งถัดไป เว็บไซต์จะจดจำได้ว่าเป็นผู้ใช้ที่เคยเข้าใช้บริการแล้ว และตั้งค่าตามที่เจ้าของข้อมูลส่วนบุคคลกำหนด จนกว่าเจ้าของข้อมูลส่วนบุคคลจะลบคุกกี้ (Cookies) หรือไม่อนุญาตให้คุกกี้ (Cookies) นั้นทำงานอีกต่อไป ซึ่งเจ้าของข้อมูลส่วนบุคคลสามารถที่จะยอมรับหรือไม่รับคุกกี้ (Cookies) ก็ได้ ในกรณีที่เลือกที่จะไม่รับหรือลบคุกกี้ (Cookies) เว็บไซต์อาจจะไม่สามารถให้บริการหรือไม่สามารถแสดงผลได้อย่างถูกต้อง
    </label>
</div>
<div class="mb-2" name="consent_9">
    <h5>9. การปรับปรุงนโยบายการคุ้มครองข้อมูลส่วนบุคคล</h5>
    <label>
    ร้านค้าอาจทำการปรับปรุงหรือแก้ไขนโยบายการคุ้มครองข้อมูลส่วนบุคคล โดยมิต้องแจ้งให้เจ้าของข้อมูลส่วนบุคคลทราบล่วงหน้า ทั้งนี้ เพื่อความเหมาะสมและมีประสิทธิภาพในการให้บริการ ดังนั้น ร้านค้าจึงขอแนะนำให้เจ้าของข้อมูลส่วนบุคคลอ่านนโยบายการคุ้มครองข้อมูลส่วนบุคคลทุกครั้งที่เยี่ยมชมหรือใช้บริการจากร้านค้าหรือเว็บไซต์ของร้านค้า
    </label>
</div>